#include "libft.h"

char	*ft_strncat(char *dst, const char *src, size_t n)
{
	size_t	i;

	i = 0;
	while (*(dst + i) != '\0')
		i++;
	while (*src && n--)
		*(dst + i++) = *src++;
	*(dst + i) = '\0';
	return (dst);
}
